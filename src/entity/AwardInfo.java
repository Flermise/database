package entity;

public class AwardInfo {
	private int awdInfoId;
	private int awdId;
	private String awdInfoName;

	public int getAwdInfoId() {
		return awdInfoId;
	}

	public void setAwdInfoId(int awdInfoId) {
		this.awdInfoId = awdInfoId;
	}

	public int getAwdId() {
		return awdId;
	}

	public void setAwdId(int awdId) {
		this.awdId = awdId;
	}

	public String getAwdInfoName() {
		return awdInfoName;
	}

	public void setAwdInfoName(String awdInfoName) {
		this.awdInfoName = awdInfoName;
	}
	
	
}
