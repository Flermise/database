package entity;

public class CelebAwardInfo {
	private int awdInfoId;
	private int celebId;
	private String state;

	public int getAwdInfoId() {
		return awdInfoId;
	}

	public void setAwdInfoId(int awdInfoId) {
		this.awdInfoId = awdInfoId;
	}

	public int getCelebId() {
		return celebId;
	}

	public void setCelebId(int celebId) {
		this.celebId = celebId;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}
	
}
