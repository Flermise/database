package entity;

public class Celebrity {
	private int celebId;
	private String chName;
	private String enName;
	public String otherChName;
	public String otherEnName;
	public String avatar;
	public String sex;
	public String constellation;
	public String birthplace;
	public String birthday;
	public String deathday;
	public String familyMember;
	public String IMDb;
	public String intro;

	public int getCelebId() {
		return celebId;
	}

	public void setCelebId(int celebId) {
		this.celebId = celebId;
	}

	public String getChName() {
		return chName;
	}

	public void setChName(String chName) {
		this.chName = chName;
	}

	public String getEnName() {
		return enName;
	}

	public void setEnName(String enName) {
		this.enName = enName;
	}

	public String getOtherChName() {
		return otherChName;
	}

	public void setOtherChName(String otherChName) {
		this.otherChName = otherChName;
	}

	public String getOtherEnName() {
		return otherEnName;
	}

	public void setOtherEnName(String otherEnName) {
		this.otherEnName = otherEnName;
	}

	public String getAvatar() {
		return avatar;
	}

	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public String getConstellation() {
		return constellation;
	}

	public void setConstellation(String constellation) {
		this.constellation = constellation;
	}

	public String getBirthplace() {
		return birthplace;
	}

	public void setBirthplace(String birthplace) {
		this.birthplace = birthplace;
	}

	public String getBirthday() {
		return birthday;
	}

	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}

	public String getDeathday() {
		return deathday;
	}

	public void setDeathday(String deathday) {
		this.deathday = deathday;
	}

	public String getFamilyMember() {
		return familyMember;
	}

	public void setFamilyMember(String familyMember) {
		this.familyMember = familyMember;
	}

	public String getIMDb() {
		return IMDb;
	}

	public void setIMDb(String iMDb) {
		IMDb = iMDb;
	}

	public String getIntro() {
		return intro;
	}

	public void setIntro(String intro) {
		this.intro = intro;
	}
}
